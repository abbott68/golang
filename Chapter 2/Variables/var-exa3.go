package main

import "fmt"

func main() {
	// 定义整数类型常量
	const age int = 30
	fmt.Println("年龄:", age)

	// 定义浮点数类型常量
	const price float64 = 29.99
	fmt.Println("价格:", price)

	// 定义字符串类型常量
	const name string = "John Doe"
	fmt.Println("姓名:", name)

	// 定义布尔类型常量
	const isStudent bool = true
	fmt.Println("是否是学生:", isStudent)
}
