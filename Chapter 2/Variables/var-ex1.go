package main

import "fmt"

func main() {
	// 定义整数类型变量
	var age int = 30
	fmt.Println("年龄", age)

	// 定义浮点数类型变量
	var price float64 = 39.99
	fmt.Println("价格；", price)

	// 定义字符串类型变量
	var name string = "john Don"
	fmt.Println("姓名：", name)

	// 定义布尔类型变量
	var isStudent bool = true
	fmt.Println("是否是学生：", isStudent)
}
